//   lambda[0]=10.7461931799
//   lambda[1]=9.2105275883
//   lambda[2]=8.0350936446
//   lambda[3]=6.9649063554
//   lambda[4]=5.7894724117
//   lambda[5]=4.2538068201
// ============================================================
module tb_qr_eigen_top;
parameter N=6;
reg clk,rst,start;
reg [64*N*N-1:0] A_in;
wire [64*N-1:0] eigenvalues;
wire [64*N*N-1:0] V_flat;
wire [64*N-1:0] sigma_flat;
wire [64*N*N-1:0] U_flat,SV_flat;
wire done;
qr_eigen_top #(.N(N)) dut(
    .clk(clk),.rst(rst),.start(start),.A_in(A_in),
    .eigenvalues(eigenvalues),.V_flat(V_flat),
    .sigma_flat(sigma_flat),.U_flat(U_flat),.SV_flat(SV_flat),.done(done)
);
initial clk=0; always #5 clk=~clk;
real eig_val[0:N-1];
real sig_val[0:N-1];
integer ii;
initial begin
    rst=1; start=0;
    A_in[     0+:64]=64'h4014000000000000; // [0][0]= 5.0000
    A_in[    64+:64]=64'hBFF0000000000000; // [0][1]=-1.0000
    A_in[   128+:64]=64'h0000000000000000; // [0][2]= 0.0000
    A_in[   192+:64]=64'h0000000000000000; // [0][3]= 0.0000
    A_in[   256+:64]=64'h0000000000000000; // [0][4]= 0.0000
    A_in[   320+:64]=64'h0000000000000000; // [0][5]= 0.0000
    A_in[   384+:64]=64'hBFF0000000000000; // [1][0]=-1.0000
    A_in[   448+:64]=64'h4018000000000000; // [1][1]= 6.0000
    A_in[   512+:64]=64'hBFF0000000000000; // [1][2]=-1.0000
    A_in[   576+:64]=64'h0000000000000000; // [1][3]= 0.0000
    A_in[   640+:64]=64'h0000000000000000; // [1][4]= 0.0000
    A_in[   704+:64]=64'h0000000000000000; // [1][5]= 0.0000
    A_in[   768+:64]=64'h0000000000000000; // [2][0]= 0.0000
    A_in[   832+:64]=64'hBFF0000000000000; // [2][1]=-1.0000
    A_in[   896+:64]=64'h401C000000000000; // [2][2]= 7.0000
    A_in[   960+:64]=64'hBFF0000000000000; // [2][3]=-1.0000
    A_in[  1024+:64]=64'h0000000000000000; // [2][4]= 0.0000
    A_in[  1088+:64]=64'h0000000000000000; // [2][5]= 0.0000
    A_in[  1152+:64]=64'h0000000000000000; // [3][0]= 0.0000
    A_in[  1216+:64]=64'h0000000000000000; // [3][1]= 0.0000
    A_in[  1280+:64]=64'hBFF0000000000000; // [3][2]=-1.0000
    A_in[  1344+:64]=64'h4020000000000000; // [3][3]= 8.0000
    A_in[  1408+:64]=64'hBFF0000000000000; // [3][4]=-1.0000
    A_in[  1472+:64]=64'h0000000000000000; // [3][5]= 0.0000
    A_in[  1536+:64]=64'h0000000000000000; // [4][0]= 0.0000
    A_in[  1600+:64]=64'h0000000000000000; // [4][1]= 0.0000
    A_in[  1664+:64]=64'h0000000000000000; // [4][2]= 0.0000
    A_in[  1728+:64]=64'hBFF0000000000000; // [4][3]=-1.0000
    A_in[  1792+:64]=64'h4022000000000000; // [4][4]= 9.0000
    A_in[  1856+:64]=64'hBFF0000000000000; // [4][5]=-1.0000
    A_in[  1920+:64]=64'h0000000000000000; // [5][0]= 0.0000
    A_in[  1984+:64]=64'h0000000000000000; // [5][1]= 0.0000
    A_in[  2048+:64]=64'h0000000000000000; // [5][2]= 0.0000
    A_in[  2112+:64]=64'h0000000000000000; // [5][3]= 0.0000
    A_in[  2176+:64]=64'hBFF0000000000000; // [5][4]=-1.0000
    A_in[  2240+:64]=64'h4024000000000000; // [5][5]=10.0000
    repeat(5) @(posedge clk); #1;
    rst=0; repeat(3) @(posedge clk); #1;
    start=1; @(posedge clk); #1; start=0;
    $display("6x6 SYMMETRIC tridiagonal negative off-diagonal");
    fork
        begin:wd wait(done); disable tb; end
        begin:tb repeat(200000000) @(posedge clk);
            $display("TIMEOUT"); disable wd; end
    join
    repeat(3) @(posedge clk);
    for(ii=0;ii<N;ii=ii+1) begin
        eig_val[ii]=$bitstoreal(eigenvalues[ii*64+:64]);
        sig_val[ii]=$bitstoreal(sigma_flat[ii*64+:64]);
    end
    $display("==============================================");
    $display("  EVD RESULTS (6x6) eigenvalues descending");
    $display("==============================================");
    for(ii=0;ii<N;ii=ii+1)
        $display("  lambda[%0d] = %16.10f",ii,eig_val[ii]);
    $display("==============================================");
    $display("  SVD RESULTS (6x6) singular values");
    $display("==============================================");
    for(ii=0;ii<N;ii=ii+1)
        $display("  sigma[%0d]  = %16.10f",ii,sig_val[ii]);
    $display("==============================================");
    #10; $finish;
end
endmodule
